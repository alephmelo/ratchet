"""
Benchmark harness for the HTTP request handler.
Measures: requests_per_sec (throughput) and latency_p99 (tail latency).
Also validates correctness of all responses.

DO NOT EDIT THIS FILE — it is read-only for the agent.
"""

import random
import time
import json
import hashlib

# ---------------------------------------------------------------------------
# Deterministic setup
# ---------------------------------------------------------------------------
SEED = 42
rng = random.Random(SEED)

NUM_REQUESTS = 5000  # total requests per benchmark run
NUM_TIMED_RUNS = 5  # how many timed runs to average
WARMUP_RUNS = 1  # untimed warmup runs

# ---------------------------------------------------------------------------
# Generate a fixed request workload
# ---------------------------------------------------------------------------


def make_workload(n, rng):
    """Generate a deterministic mixed workload of n HTTP requests."""
    requests = []
    for _ in range(n):
        r = rng.random()
        if r < 0.05:
            # 5% health checks
            requests.append({"method": "GET", "path": "/health", "body": None})
        elif r < 0.40:
            # 35% user lookups
            uid = rng.randint(0, 9999)
            requests.append({"method": "GET", "path": f"/users/{uid}", "body": None})
        elif r < 0.55:
            # 15% user creation
            body = {
                "name": f"NewUser{rng.randint(0, 99999)}",
                "age": rng.randint(18, 80),
            }
            requests.append({"method": "POST", "path": "/users", "body": body})
        elif r < 0.75:
            # 20% search queries (expensive — brute force over 10k IDs)
            q = str(rng.randint(0, 999))
            requests.append({"method": "GET", "path": f"/search?q={q}", "body": None})
        elif r < 0.85:
            # 10% 404s
            requests.append({"method": "GET", "path": "/nonexistent", "body": None})
        else:
            # 15% more user lookups
            uid = rng.randint(0, 9999)
            requests.append({"method": "GET", "path": f"/users/{uid}", "body": None})
    return requests


def expected_response(req):
    """Compute the expected correct response for a given request."""
    method = req["method"]
    path = req["path"]
    body = req.get("body")

    if method == "GET" and path == "/health":
        return {"status": 200, "body": {"ok": True}}

    if method == "GET" and path.startswith("/users/"):
        uid = path.split("/")[-1]
        return {
            "status": 200,
            "body": {
                "id": uid,
                "name": f"User {uid}",
                "email": f"user{uid}@example.com",
            },
        }

    if method == "POST" and path == "/users":
        body_str = json.dumps(body, sort_keys=True)
        uid = hashlib.md5(body_str.encode()).hexdigest()[:8]
        return {"status": 201, "body": {"id": uid, "created": True}}

    if method == "GET" and path.startswith("/search"):
        q = ""
        if "?" in path:
            params = path.split("?", 1)[1]
            for param in params.split("&"):
                if param.startswith("q="):
                    q = param[2:]
                    break
        results = [i for i in range(10000) if q in str(i)]
        return {"status": 200, "body": {"query": q, "results": results}}

    return {"status": 404, "body": {"error": "not found"}}


# ---------------------------------------------------------------------------
# Main benchmark
# ---------------------------------------------------------------------------


def main():
    from handler import handle_requests

    workload = make_workload(NUM_REQUESTS, rng)

    # Pre-compute expected responses for correctness checking
    expected = [expected_response(req) for req in workload]

    # --- Warmup ---
    for _ in range(WARMUP_RUNS):
        handle_requests(workload)

    # --- Timed runs ---
    # We measure per-request latency by timing individual small batches
    # to capture p99 latency distribution
    BATCH_SIZE = 50  # simulate "concurrent" batches
    batches = [
        workload[i : i + BATCH_SIZE] for i in range(0, len(workload), BATCH_SIZE)
    ]

    all_latencies = []  # per-batch latencies in ms
    total_requests = 0
    total_elapsed = 0.0

    for run in range(NUM_TIMED_RUNS):
        run_latencies = []
        run_start = time.perf_counter()
        for batch in batches:
            batch_start = time.perf_counter()
            responses = handle_requests(batch)
            batch_end = time.perf_counter()

            batch_ms = (batch_end - batch_start) * 1000.0
            # Per-request latency for this batch
            per_req_ms = batch_ms / len(batch) if batch else 0
            run_latencies.append(per_req_ms)
        run_end = time.perf_counter()

        total_elapsed += run_end - run_start
        total_requests += NUM_REQUESTS
        all_latencies.extend(run_latencies)

    # --- Correctness ---
    # Do one final correctness check
    final_responses = handle_requests(workload)
    correct = 0
    for i, (resp, exp) in enumerate(zip(final_responses, expected)):
        if resp == exp:
            correct += 1
    correctness = (correct / len(expected)) * 100.0

    # --- Compute metrics ---
    requests_per_sec = total_requests / total_elapsed if total_elapsed > 0 else 0

    # p99 latency from all batch latencies
    all_latencies.sort()
    p99_idx = int(len(all_latencies) * 0.99)
    latency_p99 = all_latencies[min(p99_idx, len(all_latencies) - 1)]

    # --- Output ---
    print(f"requests_per_sec: {requests_per_sec:.1f}")
    print(f"latency_p99: {latency_p99:.3f}")
    print(f"correctness: {correctness:.1f}")
    print(f"total_requests: {total_requests}")
    print(f"elapsed_sec: {total_elapsed:.3f}")
    print(f"num_batches_measured: {len(all_latencies)}")
    print(f"batch_size: {BATCH_SIZE}")


if __name__ == "__main__":
    main()
