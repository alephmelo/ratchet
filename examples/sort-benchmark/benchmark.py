#!/usr/bin/env python3
"""
Immutable benchmark harness for sort.py.

Generates random arrays of varying sizes, calls sort.my_sort() on each,
verifies correctness, and reports throughput + correctness metrics.

DO NOT EDIT THIS FILE — only sort.py is editable.
"""

import random
import time
import sys

# Fix the seed so results are reproducible across runs
random.seed(42)

# Generate test data once: 200 arrays of size 500
NUM_ARRAYS = 200
ARRAY_SIZE = 500
test_data = [
    random.sample(range(ARRAY_SIZE * 10), ARRAY_SIZE) for _ in range(NUM_ARRAYS)
]

# Pre-compute expected sorted outputs
expected = [sorted(arr) for arr in test_data]

# Import the contestant's sort function
try:
    from sort import my_sort
except ImportError:
    print("ERROR: could not import my_sort from sort.py", file=sys.stderr)
    sys.exit(1)

# Warm-up run (not timed)
_ = my_sort(list(test_data[0]))

# Timed benchmark
correct = 0
start = time.perf_counter()

for i, arr in enumerate(test_data):
    result = my_sort(list(arr))  # pass a copy so sort.py can mutate in-place
    if result == expected[i]:
        correct += 1

elapsed = time.perf_counter() - start

throughput = NUM_ARRAYS / elapsed
correctness = correct / NUM_ARRAYS * 100.0

print(f"throughput: {throughput:.2f}")
print(f"correctness: {correctness:.1f}")
print(f"elapsed_sec: {elapsed:.4f}")
print(f"num_arrays: {NUM_ARRAYS}")
print(f"array_size: {ARRAY_SIZE}")
